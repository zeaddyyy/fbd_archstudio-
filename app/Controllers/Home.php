<?php

namespace App\Controllers;

use App\Models\SettingModel;
use App\Models\HomepageProjectModel;

class Home extends BaseController
{
    public function index()
    {
        /*
        |--------------------------------------------------------------------------
        | MODELS
        |--------------------------------------------------------------------------
        */

        $settingModel =
        new SettingModel();

        $homepageProjectModel =
        new HomepageProjectModel();

        /*
        |--------------------------------------------------------------------------
        | DATA
        |--------------------------------------------------------------------------
        */

        $data['setting'] =
        $settingModel->first();

        /*
        |--------------------------------------------------------------------------
        | HOMEPAGE PROJECTS
        |--------------------------------------------------------------------------
        */

        $data['projects'] =
        $homepageProjectModel
        ->orderBy(
            'id',
            'DESC'
        )
        ->findAll();

        /*
        |--------------------------------------------------------------------------
        | VIEW
        |--------------------------------------------------------------------------
        */

        return view(
            'home',
            $data
        );
    }

